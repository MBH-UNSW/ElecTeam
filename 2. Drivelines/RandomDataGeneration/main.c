/** generates a 32-bit random number using RNG peripheral
  * then converts the random number into an array fo invidual bits for transmission and LED testing
  */
void Generate_Random_Bit_Array(void)
{
  if (HAL_RNG_GenerateRandomNumber(&hrng, (uint32_t*)&random_number) != HAL_OK)
  {
    Error_Handler();
  }

  for (int i = 0; i < RANDOM_ARRAY_SIZE; i++)
  {
    /* shifted right by i bits + then mask everything except the lowest bit this works because
     * if you AND a bit with 1, you get the bit value
     */
    random_number_bits[i] = (random_number >> i) & 0x01;
  }
}


/** transmits the generated random number over USART2 to a serial terminal
  */
void USART_Send_Bit_Sequence(int i)
{
  char buffer[20];

  int len = snprintf(
    buffer,
    sizeof(buffer),
    ">index:%d,bit:%d\r\n",
    i,
    random_number_bits[i]
  );

  HAL_UART_Transmit(
    &huart2,
    (uint8_t*)buffer,
    len,
    100
  );
}


/** transmits the random bit sequence by driving the selected GPIO pin HIGH or LOW
  */
void Sequence_Output(GPIO_TypeDef *GPIOx, uint16_t GPIO_Pin, uint32_t DELAY)
{
  for (int i = 0; i < RANDOM_ARRAY_SIZE; i++)
  {
    if (random_number_bits[i] == BIT_HIGH)
    {
      /* on bank x, set pin to output logic HIGH for bit value 1 */
      HAL_GPIO_WritePin(
        GPIOx,
        GPIO_Pin,
        GPIO_PIN_SET
      );
    }
    else
    {
      /* on bank x, set pin to output logic LOW for bit value 0 */
      HAL_GPIO_WritePin(
        GPIOx,
        GPIO_Pin,
        GPIO_PIN_RESET
      );
    }

    USART_Send_Bit_Sequence(i);

    HAL_Delay(DELAY);
  }

  HAL_GPIO_WritePin(
    GPIOx,
    GPIO_Pin,
    GPIO_PIN_RESET
  );
}

